course = "Python Programming"

# makes string upper case
print(course.upper())

# makes string lower case
print(course.lower())

# capitalizes starting letters
print(course.title())

# strips the white space from the string
print(course.strip())

# finds the index number of the string
print(course.find("gram"))

# replaces string with a different string
print(course.replace("P", "S"))

# boolean checks if characters exist in string
print("ro" in course)

# boolean checks if characters do NOT exist
print("sausage" not in course)
