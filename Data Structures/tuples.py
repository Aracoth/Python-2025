# tuple
point = 1, 2
# comma after number makes a tuple
point1 = (1,)
# without a comma, it's just an integer
point2 = 1
# empty parenthesis creates a tuple
point3 = ()
# parenthesis around number, then a comma, is a tuple
point4 = (1, 2)
# parenthesis around single number is an int
point5 = 1
print(type(point5))
